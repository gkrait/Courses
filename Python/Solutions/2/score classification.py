n=eval(input("Enter the score "))
if n < 10:
    print("Fail")
if 10<=n<12:
    print("Pass")    
if 12 <=n< 14:
    print("Satisfactory")
if   14 <=n< 16:
    print("Good")
if   16 <=n< 18:
    print("Very Good")
if   18 <=n< 20:
    print("Excellent")
if n> 20:
    print("The score is at most 20")
